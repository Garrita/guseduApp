package com.gusedu.entidad;

import java.util.Date;

/**
 *
 * @author user
 */
public class ECajaResumenMensual {
    
    private Date fecha;
    private double monto;

    public ECajaResumenMensual() {
    }

    public ECajaResumenMensual(Date fecha, double monto) {
        this.fecha = fecha;
        this.monto = monto;
    }

    public Date getFecha() {
        return fecha;
    }

    public void setFecha(Date fecha) {
        this.fecha = fecha;
    }

    public double getMonto() {
        return monto;
    }

    public void setMonto(double monto) {
        this.monto = monto;
    }
    
    
}
